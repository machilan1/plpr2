CREATE TYPE file_generate_reasons AS ENUM ('employeeProfile', 'salaryForm');

CREATE TABLE files
(
    file_id           serial PRIMARY KEY,
    path              text NOT NULL UNIQUE CHECK (path <> ''),
    original_filename text NOT NULL CHECK (original_filename <> ''),
    mime_type         text NOT NULL CHECK (mime_type <> ''),
    size              int NOT NULL CHECK (size >= 0),
    created_at        timestamp with time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at        timestamp with time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,

    file_generate_reason file_generate_reasons,
    file_description text NOT NULL DEFAULT ''
);

CREATE TABLE IF NOT EXISTS cities
(
    city_id   smallserial PRIMARY KEY,
    city_name text NOT NULL UNIQUE
);

-- Might consider add some unique constraints for better data integrity
